package com.tothenew.Ques9;

class Furniture {
    boolean isWooden;
    boolean isMetal;
    int size;
    String colour;
    double weight;
    double density;
    boolean isFire;
    boolean isStress;

    public Furniture(boolean isWooden, boolean isMetal, int size, String colour, double weight, double density, boolean isFire, boolean isStress) {
        this.isWooden = isWooden;
        this.isMetal = isMetal;
        this.size = size;
        this.colour = colour;
        this.weight = weight;
        this.density = density;
        this.isFire = isFire;
        this.isStress = isStress;
    }
}
